    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
            NhanVien nv = new NhanVien();
            nv.setHoTenNV(txthoTen.getText());
            nv.setGioiTinh(txtgioiTinh.getText());
            try {
                nv.setNamSinh(sdf.parse(txtNamSinh.getText()));
            } catch (ParseException ex) {
                Logger.getLogger(F_nhanVien.class.getName()).log(Level.SEVERE, null, ex);
            }
            nv.setChucVu(txtchucVu.getText());
            nv.setLuongCB(Integer.parseInt(txtluongCB.getText()));
            nv.setNgaycong(Integer.parseInt(txtngayCong.getText()));
            DAO_nhanVien.them(nv);
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            hienthidsnv();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        int n = tbdsNV.getSelectedRow();
        if (n == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần xóa");
        } else {
            int k = JOptionPane.showConfirmDialog(this, "Thông báo", "Bạn có muốn xóa không?", JOptionPane.OK_CANCEL_OPTION);
            if (k == JOptionPane.CANCEL_OPTION) {
                return;
            } else {
                int manv = Integer.parseInt(txtmaNV.getText());
                DAO_nhanVien.xoaNV(manv);
                JOptionPane.showMessageDialog(this, "Xóa thành công");
                hienthidsnv();
            }

        }
    }//GEN-LAST:event_btnxoaActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        NhanVien nv = new NhanVien();
        nv.setMaNV(Integer.parseInt(txtmaNV.getText()));
        nv.setHoTenNV(txthoTen.getText());
        nv.setGioiTinh(txtgioiTinh.getText());
        try {
            nv.setNamSinh(sdf.parse(txtNamSinh.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(F_nhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
        nv.setChucVu(txtchucVu.getText());
        nv.setLuongCB(Integer.parseInt(txtluongCB.getText()));
        nv.setNgaycong(Integer.parseInt(txtngayCong.getText()));

        if (DAO_nhanVien.kiemTramadatontai(nv.getMaNV()) == true) {
            int k = JOptionPane.showConfirmDialog(this, "Thông báo", "Bạn có muốn cập nhật thông tin nhân viên?", JOptionPane.YES_NO_OPTION);
            if (k == JOptionPane.YES_OPTION) {
                DAO_nhanVien.sua(nv);
            }
            if (k == JOptionPane.NO_OPTION) {
                return;
            }
        }
        hienthidsnv();
    }//GEN-LAST:event_btnsuaActionPerformed

