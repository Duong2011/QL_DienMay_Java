    private void hienthidsnv() {
        ArrayList<NhanVien> dsNV = DAO_nhanVien.layDanhSachNV();
        dtm.setRowCount(0);
        for (NhanVien nv : dsNV) {
            Vector<Object> v = new Vector<Object>();
            v.add(nv.getMaNV());
            v.add(nv.getHoTenNV());
            v.add(nv.getGioiTinh());
            v.add(nv.getNamSinh());
            v.add(nv.getChucVu());
            v.add(nv.getLuongCB());
            v.add(nv.getNgaycong());
            v.add(nv.getTienluong());
            dtm.addRow(v);
        }
        tbdsNV.setModel(dtm);

    }
